package cli

import (
	"bytes"
	"context"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"strings"
	"text/template"

	"github.com/iancoleman/strcase"

	C "github.com/urfave/cli/v3"
)

const (
	keyEmbedSrc     = "src"
	keyEmbedDst     = "dst"
	keyEmbedPackage = "package"
	keyEmbedMapName = "map-name"
)

var (
	flagEmbedSrc = &C.StringFlag{
		Name:     keyEmbedSrc,
		Usage:    "Source directory to embed files from",
		Required: true,
	}

	flagEmbedDst = &C.StringFlag{
		Name:     keyEmbedDst,
		Usage:    "Destination filename for the generated Go file",
		Required: true,
	}

	flagEmbedPackage = &C.StringFlag{
		Name:  keyEmbedPackage,
		Usage: "Package name for the generated Go file (default: inferred from destination path)",
	}

	flagEmbedMapName = &C.StringFlag{
		Name:  keyEmbedMapName,
		Value: "Files",
		Usage: "Name of the map variable (default: Files)",
	}
)

// EmbedCommand returns the CLI command for generating embed declarations
func EmbedCommand() *C.Command {
	return &C.Command{
		Name:  "embed",
		Usage: "Generate Go embed declarations for files in a directory",
		Description: `Generate a Go file with embed directives for all files in the source directory.
Each file will have a corresponding embed field with a constant case name, and a map
will be created to access files by their relative path.`,
		Flags: []C.Flag{
			flagEmbedSrc,
			flagEmbedDst,
			flagEmbedPackage,
			flagEmbedMapName,
		},
		Action: func(ctx context.Context, cmd *C.Command) error {
			srcDir := cmd.String(keyEmbedSrc)
			dstFile := cmd.String(keyEmbedDst)
			pkgName := cmd.String(keyEmbedPackage)
			mapName := cmd.String(keyEmbedMapName)

			if srcDir == "" {
				return fmt.Errorf("source directory is required")
			}
			if dstFile == "" {
				return fmt.Errorf("destination file is required")
			}

			return generateEmbedFile(srcDir, dstFile, pkgName, mapName)
		},
	}
}

// fileInfo holds information about a file to be embedded
type fileInfo struct {
	RelativePath string
	FieldName    string
	EmbedPath    string
}

// generateEmbedFile generates a Go file with embed declarations
func generateEmbedFile(srcDir, dstFile, pkgName, mapName string) error {
	// Verify source directory exists
	srcInfo, err := os.Stat(srcDir)
	if err != nil {
		return fmt.Errorf("failed to stat source directory: %w", err)
	}
	if !srcInfo.IsDir() {
		return fmt.Errorf("source is not a directory: %s", srcDir)
	}

	// Collect all files
	var files []fileInfo
	err = filepath.Walk(srcDir, func(path string, info os.FileInfo, err error) error {
		if err != nil {
			return err
		}
		if info.IsDir() {
			return nil
		}

		// Get relative path
		relPath, err := filepath.Rel(srcDir, path)
		if err != nil {
			return fmt.Errorf("failed to get relative path: %w", err)
		}

		// Convert to forward slashes for embed directive
		embedPath := filepath.ToSlash(filepath.Join(srcDir, relPath))
		relPathSlash := filepath.ToSlash(relPath)

		files = append(files, fileInfo{
			RelativePath: relPathSlash,
			FieldName:    toConstantCase(relPathSlash),
			EmbedPath:    embedPath,
		})

		return nil
	})
	if err != nil {
		return fmt.Errorf("failed to walk source directory: %w", err)
	}

	if len(files) == 0 {
		return fmt.Errorf("no files found in source directory: %s", srcDir)
	}

	// Generate the Go file
	return writeEmbedFile(dstFile, files, pkgName, mapName)
}

// toConstantCase converts a file path to CONSTANT_CASE
func toConstantCase(path string) string {
	return strcase.ToScreamingSnake(strings.ReplaceAll(path, "/", "_"))
}

// writeEmbedFile writes the generated Go file
func writeEmbedFile(dstFile string, files []fileInfo, pkgName, mapName string) error {
	// Determine package name from destination file if not provided
	if pkgName == "" {
		pkgName = "main"
		if dir := filepath.Dir(dstFile); dir != "." && dir != "" {
			pkgName = filepath.Base(dir)
		}
	}

	// Template for the generated file
	tmpl := template.Must(template.New("embed").Parse(`// Code generated by fp-go embed command. DO NOT EDIT.

package {{.PackageName}}

import _ "embed"

{{range .Files}}
//go:embed {{.EmbedPath}}
var {{.FieldName}} []byte
{{end}}

// {{.MapName}} is a map of relative file paths to their embedded content
var {{.MapName}} = map[string][]byte{
{{range .Files}}	"{{.RelativePath}}": {{.FieldName}},
{{end}}}
`))

	// Execute template
	var buf bytes.Buffer
	data := struct {
		PackageName string
		MapName     string
		Files       []fileInfo
	}{
		PackageName: pkgName,
		MapName:     mapName,
		Files:       files,
	}

	if err := tmpl.Execute(&buf, data); err != nil {
		return fmt.Errorf("failed to execute template: %w", err)
	}

	// Format the generated code
	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		// If formatting fails, write unformatted code for debugging
		fmt.Fprintf(os.Stderr, "Warning: failed to format generated code: %v\n", err)
		formatted = buf.Bytes()
	}

	// Create destination directory if needed
	if dir := filepath.Dir(dstFile); dir != "." && dir != "" {
		if err := os.MkdirAll(dir, 0755); err != nil {
			return fmt.Errorf("failed to create destination directory: %w", err)
		}
	}

	// Write the file
	if err := os.WriteFile(dstFile, formatted, 0644); err != nil {
		return fmt.Errorf("failed to write destination file: %w", err)
	}

	fmt.Printf("Successfully generated embed file: %s\n", dstFile)
	fmt.Printf("Embedded %d files from %s\n", len(files), files[0].EmbedPath[:strings.LastIndex(files[0].EmbedPath, "/")])
	return nil
}
